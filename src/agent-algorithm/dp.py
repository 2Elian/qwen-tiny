# 要计算原问题 f(10)，就需要先计算出子问题 f(9) 和 f(8).然后要计算 f(9)，又要先算出子问题 f(8) 和 f(7)，以此类推。
# 一个子问题时间 = f（n-1）+f（n-2），也就是一个加法的操作，所以复杂度是 O(1)；问题个数 = 递归树节点的总数，递归树的总节点 = 2^n-1，所以是复杂度O(2^n)。
# 所以青蛙跳的时间复杂度是O(2^n)。
# 原因：比如f（8）被计算了两次，f（7）被重复计算了3次...所以这个递归算法低效的原因，就是存在大量的重复计算！
# 解决方法：算过的就不再重复计算了

# 带备忘录的递归解法（自顶向下）
"""
一般使用一个数组或者一个哈希map充当这个备忘录。
备忘录: {}
第一步：计算 f(10)
    f(10) = f(9) + f(8)
            ↓        ↓
        需要算   需要算

    备忘录: {}

    当前调用栈:
    ├── f(10) 需要 f(9) 和 f(8)
第二步：展开 f(9)
    f(9) = f(8) + f(7)

    此时发现 f(8) 被重复需要（f(10)也要，f(9)也要）

    备忘录: {} 

    当前调用栈:
    ├── f(10) 等待 f(9) 和 f(8)
        ├── f(9) 等待 f(8) 和 f(7)
第三步：展开 f(8)（来自 f(9) 的调用）
    f(8) = f(7) + f(6)

    备忘录: {} 

    开始计算结果并填入备忘录...
第四步：计算到底层，开始填入备忘录
    计算到 f(1)=1, f(2)=2 (或 f(0)=0, f(1)=1 取决于定义)

    备忘录逐渐填充:
    {
        1: 1,
        2: 2,
        3: 3,    // f(3) = f(2)+f(1) = 2+1 = 3
        4: 5,    // f(4) = f(3)+f(2) = 3+2 = 5
        5: 8,
        6: 13,
        7: 21,
        8: 34,   // 计算一次后存入
        9: 55,
        10: 89
    }
                    f(10) ←─────┐
                   /    \       │
                f(9)    f(8)────┤ (查备忘录)
               /  \             │
            f(8)  f(7)────┐     │
           /  \           │     │
        f(7)  f(6)────┐   │     │
       /  \           │   │     │
    f(6)  f(5)──┐     │   │     │
   /  \         │     │   │     │
  ...           │     │   │     │
  (只计算一次)   ↓     ↓   ↓     ↓
              f(1)  f(2)...  直接返回

总节点数：约 20 个（线性增长）
"""
hashMap = {}
def qingwatiao2(target: int) -> int:
    # 初始条件
    if target == 1:
        return 1
    elif target == 2:
        return 2
    # 检查备忘录
    if target in hashMap:
        return hashMap[target]
    # 递推
    hashMap[target] = qingwatiao(target-1) + qingwatiao(target-2)
    return hashMap[target]
start_time = time.time()
print(qingwatiao2(1000))
end_time = time.time()
elapsed = end_time - start_time
print(f"耗时：{elapsed:.6f}秒")

# 动态规划：自底向上
"""
从小问题开始，迭代推导到大问题
以青蛙跳台阶问题为例（f(1)=1, f(2)=2）：

1. 状态定义
python
dp[i] = 跳到第 i 级台阶的方法数
2. 状态转移方程
python
dp[i] = dp[i-1] + dp[i-2]
dp[i-1]：最后一步跳1级

dp[i-2]：最后一步跳2级

3. 边界条件（base case）
python
dp[1] = 1  # 只有1种跳法：1
dp[2] = 2  # 有2种跳法：1+1 或 2
三、推导过程图解
计算 f(5) 的过程：
text
初始状态：
dp[1] = 1
dp[2] = 2

第1步：计算 dp[3]
dp[3] = dp[2] + dp[1] = 2 + 1 = 3
状态：[1, 2, 3, ?, ?]

第2步：计算 dp[4]
dp[4] = dp[3] + dp[2] = 3 + 2 = 5
状态：[1, 2, 3, 5, ?]

第3步：计算 dp[5]
dp[5] = dp[4] + dp[3] = 5 + 3 = 8
状态：[1, 2, 3, 5, 8]

最终结果：dp[5] = 8
"""
def qingwatiao_dp(n):
    """标准动态规划版本"""
    if n <= 2:
        return n if n > 0 else 1
    
    # 创建DP数组
    dp = [0] * (n + 1)
    
    # 边界条件
    dp[1] = 1
    dp[2] = 2
    
    # 自底向上计算
    for i in range(3, n + 1):
        dp[i] = dp[i-1] + dp[i-2]
    
    return dp[n]

# 测试
print(qingwatiao_dp(10))  # 输出: 89