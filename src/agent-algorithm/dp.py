"""
例子：
A ： "1+1+1+1+1+1+1+1 =？"
A ： "上面等式的值是多少"
B ： 计算 "8"
A : 在上面等式的左边写上 "1+" 呢？
A : "此时等式的值为多少"
B : 很快得出答案 "9"
A : "你怎么这么快就知道答案了"
A : "只要在8的基础上加1就行了"
A : "所以你不用重新计算，因为你记住了第一个等式的值为8!动态规划算法也可以说是 '记住求过的解来节省时间'"
"""
import time
# 用一个青蛙跳的例子来讲解DP

# 一个青蛙，一次能跳一个台阶 也可以跳两个台阶，那么如果台阶数量为10，请问青蛙达到第10个台阶有多少种跳法

# 思路: 假设青蛙在第十层，那么有两种情况 1是青蛙在第八层跳了2层，2是青蛙在第9层跳了一层
#       假设青蛙在第9层，那么有两种情况 1是青蛙在第7层跳了2层，2是青蛙在第8层跳了1层
#       如此下去，得到一个地推：f(n) = f(n-1) + f(n-2)

def qingwatiao(target: int) -> int:
    # 初始条件
    if target == 1:
        return 1
    elif target == 2:
        return 2
    # 递推
    return qingwatiao(target-1) + qingwatiao(target-2)
start_time = time.time()
print(qingwatiao(1000))
end_time = time.time()
elapsed = end_time - start_time
print(f"耗时：{elapsed:.6f}秒")

